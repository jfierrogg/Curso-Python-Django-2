# 1. Imprimir un mensaje inicial
print("¡Hola! Este es mi primer programa en Python.")

# 2. Declarar variables simples
nombre = "Bastián"        # Guardamos un nombre en una variable
edad = 28                 # Guardamos una edad (valor numérico)
ciudad = "Santiago"       # Guardamos la ciudad de residencia

# Mostramos cada variable en pantalla
print("Nombre:", nombre)
print("Edad:", edad)
print("Ciudad:", ciudad)

# 3. Realizar operaciones básicas
# Definimos dos números
numero_1 = 15
numero_2 = 10

# Sumamos los números
resultado_suma = numero_1 + numero_2  # Aquí hacemos la suma

# Mostramos el resultado
print("El resultado de la suma es:", resultado_suma)

# 4. Explorar un módulo integrado
import math  # Importamos el módulo math para usar funciones matemáticas

# Calculamos la raíz cuadrada de un número usando math.sqrt()
raiz = math.sqrt(25)  # sqrt calcula la raíz cuadrada

print("La raíz cuadrada de 25 es:", raiz)
