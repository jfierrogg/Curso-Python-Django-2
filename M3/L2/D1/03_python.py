# operadores aritméticos y precedencia

a = 10
b = 3

suma = a + b
resta = a - b
producto = a * b
division = a / b
division_entera = a // b
modulo = a % b
potencia = a ** b

exp1 = 2 + 3 * 4      # 3*4 primero
exp2 = (2 + 3) * 4    # paréntesis cambian el orden

print(suma, resta, producto, division)
print(division_entera, modulo, potencia)
print(exp1, exp2)
